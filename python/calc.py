""" calc.py """


def add(x, y):
    """
    :param x:
    :param y:
    :return: sum of x and y
    """
    return x + y


def difference(x, y):
    """
    :param x:
    :param y:
    :return: returns difference of x and y
    """
    return x - y


def multiply(x, y):
    """
    :param x:
    :param y:
    :return: returns product of x and y
    """
    return x * y
